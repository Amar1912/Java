public class DataTypesDemo {

    public static void main(String[] args) {

        // Primitive Data Types

        byte age = 20;
        short year = 2026;
        int marks = 95;
        long population = 8000000000L;

        float temperature = 36.5f;
        double pi = 3.14159265359;

        char grade = 'A';

        boolean isStudent = true;

        // Non-Primitive Data Type

        String name = "Amar";

        // Printing values

        System.out.println("byte value: " + age);
        System.out.println("short value: " + year);
        System.out.println("int value: " + marks);
        System.out.println("long value: " + population);

        System.out.println("float value: " + temperature);
        System.out.println("double value: " + pi);

        System.out.println("char value: " + grade);

        System.out.println("boolean value: " + isStudent);

        System.out.println("String value: " + name);
    }
}